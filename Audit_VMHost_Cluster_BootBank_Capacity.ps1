	.SYNOPSIS
		This script will audit a VMHost's Boot Bank and report Total and Used space.

	.DESCRIPTION
		Use this script to audit the Boot Bank space to verify that it has enough
		capacity for installing other VIBs including NSX-T
	.NOTES
		This script requires a VMware PowerCLI minimum version 11.4 or greater. 

	.TROUBLESHOOTING
		
